package operators;

public class Program20 {
    public static void main(String[] args) {
        int a = 10;
        System.out.println("a: " + a);
        System.out.println("Pre-Increment: (++a): " + (++a));
        System.out.println("a: " + a);
        System.out.println("Post-Increment(same line): (a++): " + (a++));
        System.out.println("Next line: " + a);
    }
}
