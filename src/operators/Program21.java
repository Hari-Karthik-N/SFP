package operators;

public class Program21 {
    public static void main(String[] args) {
        int a = 12, b = 17;
        System.out.println("++a-b-- : " + (++a-b--));
        System.out.println("a%b++ : " + (a%b++));
        System.out.println("a*=b+5 : " + (a*=b+5));
        System.out.println("69>>>2 : " + (69>>>2));
    }
}
